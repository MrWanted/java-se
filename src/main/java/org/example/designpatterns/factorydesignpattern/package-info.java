package org.example.designpatterns.factorydesignpattern;
/***
 * creational design pattern that provides an interface for creating objects in a superclass,
 * but allows subclasses to alter the type of objects that will be created.
 * It provides a way to delegate the instantiation logic to child classes.
 *
 * provides a way to create objects without specifying their concrete class
 * promotes loosely coupled code
 *
 * useful in situations where a class cannot anticipate the type of objects it needs to create,
 * or when a class wants to delegate the responsibility of object creation to its subclasses.
 */