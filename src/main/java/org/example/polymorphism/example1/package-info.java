package org.example.polymorphism.example1;

/**
 * Polymorphism is a fundamental concept in object-oriented programming that allows objects of different classes to be
 *  treated as if they belong to a common superclass or interface.
 * Polymorphism enables objects of different types to be used interchangeably, providing a way to write code that is more flexible and reusable.
 *
 * Polymorphism can be achieved through two mechanisms: inheritance and interfaces. Inheritance allows a subclass to
 *  inherit the properties and methods of a superclass, and can then add its own unique behavior or override inherited behavior.
 *  Interfaces define a set of methods that a class must implement, allowing objects of different classes to be treated as if they have the same interface.
 *
 * Polymorphism is often used to simplify code, improve maintainability, and increase flexibility.
 * It allows programmers to write code that can work with a wide range of objects, without needing to know the
 *  specific details of each object's implementation.
 */